void main() {
  num n = 8;
  switch(n) {
    case 1:
      print("value is 1");
      break;
    case 2:
      print ("value is 2");
      break;
    case 3:
      print("value is 3");
      break;
    case 4:
      print("value is 4");
      break;
    default:
      print("out of range");
      break;
  }
}
